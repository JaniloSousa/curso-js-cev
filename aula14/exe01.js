/*
JAVASCRIPT - ESTRUTURAS DE REPETIÇÕES
ESTRUTURA DE REPETIÇÃO FOR
* SINTAXE:
    for(inicio;teste;incremento) {
        ... bloco de código ... 
    }
*/
console.log("Seu programa vai começar!");
for(var c = 1; c <= 10; c++) {
    console.log(`Repetição ${c}`);
}
console.log("FIM!");

// DICA: ultilize o laço de repetição 'for' quando você souber de ante-mão quantas vezes o laço irá se repetir!
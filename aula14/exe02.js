/*
vamos fazer a contagem de 1 até 10 nos dois casos possíveis de laço
*/

console.log("Iniciando o laço while");

c=1;
while(c<=10) {
    console.log(c);
    c++;
}

console.log("Fim do laço while");
console.log("======================================");
console.log("Iniciando o laço for");

for(c=1;c<=10;c++) {
    console.log(c);
}

console.log("Fim do laço for");
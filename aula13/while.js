/*
ESTRUTURAS DE REPETIÇÕES EM JAVASCRIPT
WHILE (lê-se "enquanto") -> testa a condição e depois faz o que tem que ser feito
DO WHILE (lê-se "faça ... enquanto") -> faz o que tem que ser feito e depois testa a condição
*/

var contador = 1; // variável contadora
while(contador <= 10) { // condição
    console.log(`Passo ${contador}`); // loop
    contador++; // não se esqueça de atualizar a variável se não cai em um loop infinito
}

